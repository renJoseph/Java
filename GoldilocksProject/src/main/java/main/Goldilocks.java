package main;

public class Goldilocks {
	int weight;
	int maxTemp;
	
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}
	public int getTemperature() {
		return maxTemp;
	}
	public void setTemperature(int temperature) {
		this.maxTemp = temperature;
	}
	
}
